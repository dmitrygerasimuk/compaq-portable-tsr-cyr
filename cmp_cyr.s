;
; Program starts here
;

section .text
main:
	; Check if already installed: INT 10h points to our signature
	mov ax, 0x3510
	int 21h                    ; ES:BX = current INT 10h handler

	push cs
	pop ds
	lea si, [isr_sig]
	mov di, bx
	add di, (isr_sig - new_isr)
	mov cx, (isr_sig_end - isr_sig)
	repe cmpsb
	je uninstall_tsr

; ------------------------------------------------------------
; Install
; ------------------------------------------------------------
install_tsr:
	; Get the address of the existing INT 0x10 video interrupt vector
	mov ax, 0x3510
	int 21h
	mov [cs:old_isr], es
	mov [cs:old_isr + 2], bx
	
	; Set video mode 1 (40x25 text) to force a complete screen mode change
	mov ax, 0x1
	int 0x10
		
	; Replace interrupt vector 0x10 with our wrapper
	mov ax, cs
	mov ds, ax
	mov dx, new_isr
	mov ax, 0x2510
	int 21h

	; Set video mode 3 to test the interrupt routine
	mov ax, 0x3
	int 0x10

	jmp print_message_on

; ------------------------------------------------------------
; Uninstall: restore old INT 10h and free resident block
; ------------------------------------------------------------
uninstall_tsr:
	; ES is resident segment (from INT 10h vector). Use it to read saved vars.
	push es
	pop ds                    ; DS = resident segment

	; restore INT 10h
	mov ax, [ds:old_isr]       ; segment
	mov dx, [ds:old_isr + 2]   ; offset
	push ds
	mov ds, ax
	mov ax, 0x2510
	int 21h
	pop ds

	; free resident environment (PSP:2Ch), then free resident PSP block
	; (DOS expects the current PSP to match the owner of the block).
	mov ah, 0x51              ; get PSP -> BX
	int 0x21
	mov [cs:cur_psp], bx

	mov bx, ds                ; set PSP to resident PSP
	mov ah, 0x50
	int 0x21

	mov ax, [ds:0x2C]
	or ax, ax
	jz uninstall_skip_env_free
	mov es, ax
	mov ah, 0x49
	int 0x21
uninstall_skip_env_free:
    mov ax,ds
	mov es,ax
	xor ax,ax
	mov ah, 0x49
	int 0x21

	; restore original PSP
	mov bx, [cs:cur_psp]
	mov ah, 0x50
	int 0x21

	; print message
	push cs
	pop ds
	mov dx, msg_off
	mov ah, 9
	int 0x21

	mov ax, 0x4c00
	int 0x21

print_message_on:
	push cs
	pop ds
	mov dx, msg_on
	mov ah, 9
	int 0x21

exit:
	; 1. The program occupies:
	;    • .text: 0x1B1 bytes.
	;    • .data: 0x1000 bytes.
	;    • Total length: 0x1B1 + 0x1000 = 0x11B1 bytes (4529 bytes).
	; 2. Reserved memory for TSR must be a multiple of paragraphs (16 bytes):
	;    • Round up to the nearest larger value.

	mov ax, 0x3100        ; terminate and stay resident
    mov dx, 0x140        ; reserve 284 paragraphs
    int 0x21


;
; Function to restore the old ISR (unused)
;
restore_isr:
	mov ds, [cs:old_isr]
	mov dx, [cs:old_isr + 2]
	mov ax, 0x2510
    int 21h 
	mov ax, cs
	mov ds, ax
	ret
 
	
;
; Replacement ISR for INT 0x10 BIOS video routines
;
new_isr:
	jmp short isr_entry
isr_sig:
	db 'CMP_CYR10', 0
isr_sig_end:
isr_entry:
	mov [cs:old_ax], ax
	cmp ah, 0				; Is this a call to set the video mode? (AH=0)
	jne call_old_isr
	
	; Push a new return address on the stack so that after the BIOS routine
	; finishes, we run custom code (post_set_video_mode)
	pushf
	mov ax, cs
	push ax
	mov ax, post_set_video_mode
	push ax
	
call_old_isr:
	; Perform a far jump by pushing the old ISR address and using retf
	; This preserves all registers.
	mov ax, [cs:old_isr]
	push ax
	mov ax, [cs:old_isr + 2]
	push ax
	mov ax, [cs:old_ax]
	retf
	
;
; Called immediately after the 'set screen mode' BIOS routine has completed
;
post_set_video_mode:
	push ax
	push si
	push es
	push di
	push dx
	push cx
	
	; If the video mode call was successful, the mode number should be 
	; stored in the BIOS data area at 0x40:0x49
	mov cx, [cs:old_ax]
	mov ax, 0x40
	mov es, ax
	mov di, 0x49
	cmp cl, [es:di]
	jne skip_calibration

	cmp cl, [cs:last_screen_mode]
	jne changed
	mov [cs:last_screen_mode], cl
	jmp skip_calibration
changed:
    cmp cl, 0x03             ; AL <= 0x03 are text modes (color and mono)
    jbe installFont           ; If text mode, proceed to font installation
    cmp cl, 0x07             ; Check mode 0x07 (mono text)
    je installFont
    cmp cl, 0x09             ; Check mode 0x09 (CGA text mono)
    je installFont
    jmp skip_calibration

installFont:
    ; Installing a custom font
    mov ax,0x1100
    mov dx,0x0000
                      
    mov cx,0x0100
    mov bx,0x1000
    ; ofs and seg for cyrFont:
    
	push cs         
	pop es          
	mov di, bp  ; Загружаем смещение cyrFont в DI
	lea bp, [cyrFont+8]
	
    int 0x10

	;    VIDEO - LOAD USER-SPECIFIED LCD CHARACTER FONT (CONV,Compaq Port 386)

    ;    AX = 1400h
    ;    ES:DI -> character font
    ;    BH = number of bytes per character
    ;    08h or 10h (Compaq)
    ;    BL = 00h load main font (block 0)
    ;    01h load alternate font (block 1)
    ;    CX = number of characters to store
    ;    DX = character offset into RAM font area
    mov ax, 0x1400 
    mov dx, 0x0000
    mov cx, 0x0100
    mov bx, 0x1000
 	
	push cs
	pop es
	mov di, bp  ; Загружаем смещение cyrFont в DI
	lea bp, [cyrFont+8]
	 
    int 0x10

skip_calibration:
	pop cx
	pop dx
	pop di
	pop es
	pop si
	pop ax
	
	iret
	
